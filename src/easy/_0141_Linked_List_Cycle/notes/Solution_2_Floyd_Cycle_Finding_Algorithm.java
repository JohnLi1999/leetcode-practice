package easy._0141_Linked_List_Cycle.notes;

import utils.list_node.ListNode;

public class Solution_2_Floyd_Cycle_Finding_Algorithm {
    public boolean hasCycle(ListNode head) {
        if (head == null) {
            return false;
        }

        ListNode slow = head;
        ListNode fast = head.next;
        while (slow != fast) {
            if (fast == null || fast.next == null) {
                return false;
            }
            slow = slow.next;
            fast = fast.next.next;
        }
        return true;
    }
}
